package java101;

public class RelationalOperators {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        System.out.println(x < y);
        System.out.println(y < x);
        System.out.println(x <= y);
        System.out.println(x >= y);

        System.out.println(x == y);
        System.out.println(x != y);

        boolean b = x==y;
        System.out.println(b);

    }
}
